HAL_StatusTypeDef RTC_Set_Time(u8 hour,u8 min,u8 sec)
{
	//int len ;
	//char c[200];
	RTC_TimeTypeDef RTC_TimeStructure;
	RTC_TimeStructure.Hours=hour;
	RTC_TimeStructure.Minutes=min;
	RTC_TimeStructure.Seconds=sec;
	//RTC_TimeStructure.TimeFormat=ampm;
	RTC_TimeStructure.DayLightSaving=RTC_DAYLIGHTSAVING_NONE;
    RTC_TimeStructure.StoreOperation=RTC_STOREOPERATION_RESET;
	printf("%02d:%02d:%02d\r\n",hour, min, sec);
//	len=sprintf(c,"%02d:%02d:%02d\r\n",hour, min, sec);
//	printf("AT+CIPSEND=1,%i,\"219.128.73.196\",20030\r\n",len);
//	delay_ms(200);
//	printf("%s",c);
	return HAL_RTC_SetTime(&hrtc,&RTC_TimeStructure,RTC_FORMAT_BIN);	
}

